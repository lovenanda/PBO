public class VGA {
    private String name;
    private int kapasitas;

    public VGA(String name, int kapasitas){
        this.name = name;
        this.kapasitas = kapasitas;
    }

    public String getName(){
        return this.name;
    }

    public int getKapasitas(){
        return kapasitas;
    }
    
    
}
