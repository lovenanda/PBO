package Latihan1;

public class Titik {
    public int hitungTitik(int a, int b){
        int hasil = a + b;
        return hasil;
    }
    public  int hitungTitik(int a, int b, int c){
        int hasil = a + b + c;
        return hasil;
    }
    public String hitungTitik(String a, String b){
        String hasil = a + b;
        return hasil;
    }
    
}
